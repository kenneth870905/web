  this.gstr = function (key, ary, defaultVal) {
    if (Lauguage.hasOwnProperty(key)) {
      var str = Lauguage[key];
      if (ary) {
        for (var k in ary) {
          var v = ary[k];
          while (str.indexOf("[" + k + "]") != -1) {
            str = str.replace("[" + k + "]", v)
          }
        }
      }
      return str;
    } else {
      if (defaultVal !== undefined) {
        return defaultVal;
      } else {
        return key;
      }
    }
  }
