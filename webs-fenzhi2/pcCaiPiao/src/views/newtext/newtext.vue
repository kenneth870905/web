                       <h4>“三个全覆盖”兜牢民生保障底线</h4>

                       <p>2018年是决胜全面建成小康社会的关键一年。全面小康，一个不能少。</p>

                       <p>目前困难群众占全国总人口数的5.6%左右。黄树贤表示，党中央、国务院制定和完善困难群众基本生活保障政策，形成了比较健全的低保、医疗救助、特困供养、孤儿保障、残疾人补贴等法规政策，在制度上已经实现了对各类困难群众的应保尽保。</p>

                       <p>黄树贤介绍，按照党中央、国务院关于发挥低保和医疗救助在脱贫攻坚中的兜底作用，各级政府对困难群众基本生活保障的投入只增不减的部署要求，民政部会同扶贫办等部门不断完善农村低保兜底制度安排，实现了全国所有县（市、区）农村低保标准均达到或超过国家扶贫标准。同时，鼓励引导社会力量参与脱贫攻坚，越来越多的社会组织参与到产业扶贫、教育扶贫、健康扶贫、易地搬迁等工作。</p>

                       <p>“从现在起到2020年，实现从制度的全覆盖到实际工作、实际保障人群的全覆盖，把民生保障的底线兜住兜牢，确保全面建成小康社会时困难群众不落一户、不落一人。”黄树贤说。</p>

                      <h4>构建体系 提高质量 提升养老服务获得感 </h4>

                      <p> 截至2017年底，全国60岁以上老人达到2.41亿人，占总人口的17.3%。老龄化加剧对养老供给和养老服务提出更高要求。</p>

                       <p>黄树贤介绍，国务院出台老龄和养老规划、实施老年人照顾服务项目、放开养老服务市场等政策，民政部联合有关部门发布了养老服务标准体系建设指南和全国养老院服务质量标准框架；启动了两批居家和社区养老服务改革试点；启动开展全国养老院服务质量建设专项行动，对全国4万余家养老院进行了排查整治，处理隐患近19.7万处，依法取缔、关停、撤并养老院2122家，养老院服务质量明显改善。</p>

                      <p>黄树贤坦言，目前养老服务领域中一些老年人生活比较困难，需要加大救助保障力度；一些养老院设施落后、服务类型单一、服务质量不高；放开养老服务市场、鼓励社会力量参与的政策落实不到位；养老服务从业人员待遇偏低、人才流失严重，队伍十分短缺；相关法规政策还有待进一步健全。</p>

