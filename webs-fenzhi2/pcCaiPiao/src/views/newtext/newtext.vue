                       <h4>再续芳华 跨越千年的旅程 </h4>

                       <p>说起旅程，比起“呱呱乐”小青蛙，我们彩票的旅程要波澜壮阔得多，可说是纵贯千年，横跨全球。</p>

                       <p>据说，最早的彩票可以追溯到两干多年前的古罗马时代，那时的彩票在皇室与商界颇为流行，后来逐渐在欧洲各国盛行起来，并流传到世界各地。不过这只是传闻。真正有史可查的彩票机构产生于16世纪初的佛罗伦萨，在这之后，各国的彩票机构不断出现。1726年荷兰、1754年丹麦、1763年西班牙、1785年葡萄牙及1787年奥地利，这些国家的政府都先后把发行彩票作为新的收入来源。目前，在全世界150多个国家都能看到彩票的足迹，一些国家的彩票年销售额甚至超过百亿美元。</p>

                       <p>在我国，彩票同样经历过不平凡的旅程。元代，一些寺院就曾使用“拈阄射利”以解决建造殿堂塔院的资金问题。“拈阄”即任取事先做好记号的纸片或纸团等，以决定得什么或做什么；所谓的“利”，就是“利物”，也就是大奖。因此，简单地说，“拈阄射利”就是凭有记号的纸片获取大奖。这是我国最早带有彩票性质的活动。</p>

                       <p>光绪六年（1880年）前后，杭州人刘学询在北京会试时发行“闱姓”，其规则是将应试者的姓印在纸上，定价出售，由购买者填选中榜者的姓，发榜后，按猜中的多少依次获得头、二、三等彩。光绪年间还抽“闱姓”募集资金以助军饷。</p>

                        <img :src="dataconfig.api_urlData+'image/cpindex/img/news/1.jpg'" alt="白鸽票">

                      <p>不久，“白鸽票”诞生（又名“小闱姓”），顾名思义就是利用白鸽开彩，80个字卷成小球，白鸽衔出20个小球，就是所开的字。这种开字法简单公平，白鸽只需稍加训练就会去衔那些纸团。白鸽票在当时广为流传，并且冲出国门，传到美洲大陆，在澳门，白鸽票从诞生起一直到今天，都是热门彩票。</p>
                        <img :src="dataconfig.api_urlData+'image/cpindex/img/news/2.jpg'" alt="奏办湖北签捐票">
                      
                      

                      <p>而由政府最早发行的彩票，则是清末湖北地方政府批准发行的“奏办湖北签捐票”。此后，浙、皖、鲁、豫、湘、甘、粤等省相继发行了各省的彩票，彩票的脚步也走过了大半个中国。1918年以后，各省督军发行各种以“善后”“济实”“慈善”为名的彩票，彩票业因此得以遍布全国，出名者如“湖北军事善后有奖义券”“湘赈慈善救济券”“山东兴业票”等。</p>

                      <p>新中国的彩票业诞生于1987年，经党中央、国务院批准，中国社会福利有奖募捐委员会在京成立，这是新中国成立后，党中央、国务院批准的第一家彩票经营、管理机构，其宗旨是发扬社会主义人道主义精神，筹集社会福利资金，兴办残疾人、老年人、孤儿等福利事业和资助有困难的人。从此，新中国福利事业强有力的助力者——彩票（即“中国社会福利有奖募捐券”，后更名为“中国福利彩票”），开始了在全国范围的爱心之旅。</p>

                       <h4>生而不凡 彩票“小兵”奔赴公益前线 </h4>

                      <p>别看我只是一张小小的彩票，但是在我们从一张张白纸变为精美彩票的“出生”过程中，不仅有全世界最顶尖的科技在支持，也饱含着“福彩人”付出的辛勤汗水，正是他们的无悔付出，才让每张彩票的“出生”都成为一段不平凡的旅程，让平凡的彩票成为了福彩公益事业的“排头兵”。</p>

                      “刮刮乐”彩票最初的旅程，是在印刷设备上完成的。听负责印刷的“福彩叔叔”说过，生产我的设备是世界领先的进口彩票专用设备，能够实现21站连续接纸生产，具有高效、安全的性能特点。在数十道工序中，从奖组设计到彩票主题设计，再到防伪技术，每一个流程都有严格的监控，整个旅程都好像在军队中进行演练，认真严谨、一丝不苟。在生产过程中，设计机房与生产设备分离，杜绝人为因素的可能性，大大提高了彩票生产的安全性。在完成前期印刷、奖符喷印、奖符覆盖印刷、票面彩色印刷、折页以及检验裁切包装等环节后，我们这些刚“出生”的“彩票小兵”就会从生产车间旅行到仓储中心。</p>
                        <img :src="dataconfig.api_urlData+'image/cpindex/img/news/3.jpg'" alt="仓储间">
                        
                      

                      <p>刮刮乐仓储物流中心是我们的中转站，也是我们即将开始征程的“前哨堡垒”。在这里，我们这些“刮刮乐”彩票像即将出征的士兵一样，分门别类、整齐有序地成箱排在货架上，每个箱子上都会标明票种名称、批次等信息，俨然是部队的编号，这种包装方式称为“整托盘码放”。虽然装的都是我们这些“小兵”，但你可别小看了“前哨堡垒”，这里共有三个库区，共计占地面积6700平方米，每个库房可存放50亿元的彩票！出于安全考虑，库内有十余个红外线摄像头，24小时不间断工作，监控库房的每个角落。一旦有“外敌”想要入侵我们的“堡垒”，这些不知疲倦的“天眼”必然会揪出他们，保护我们的安全。</p>

                      <p>当我们的“指挥部”调配妥当之后，我们这些“彩票小兵”就会乘上一辆辆“战车”，前往全国各省市地区，奔赴我们新的“战场”，去完成我们的使命——为“扶老、助残、救孤、济困”而筹集更多爱心。</p>

                       <h4>下一站公益 旅行彩票很暖很佛系 </h4>

                      <p>虽然我们这些彩票奔赴的是公益“前线”，听起来军事味十足，但比起暖心和佛系，我们却一点都不输给旅行青蛙。青蛙暖的，只是游戏者自己；我们福利彩票暖的，可是全国所有需要帮助的人们，还有谁比我们更暖、更佛系？</p>

                      <p>在经过长途奔波，赶往全国各地多如繁星的福彩投注站之后，我们终于来到了福彩公益的最大支持者——彩民手中。与其他彩票相比，我们刮刮乐的优势是方便快捷，即开即中、兼中兼得，更有着高达65%的返奖率，不管是逛街、吃饭，还是旅游途中，只要出手，就在游戏的同时奉献爱心，更有机会凭借“小投入”赢得“大幸运”。广州从化的彩民谢先生在陪妻子逛街时，花20元随手刮彩，幸运斩获“百发百中 点球大战”一等奖20万元；新乡彩民张先生在和朋友去吃饭的路上，用身上的零钱买了几张刮刮乐“发奖金”，竟真的收获12万元的丰厚“奖金”；广西彩民陈先生趁着春节假期到顺德探亲访友，被路边投注站里热闹的气氛吸引，花90元购买“5倍惊喜”，结果被20万元的“大惊喜”砸了个正着……这样无意购彩中大奖的例子不胜枚举，看着彩民们因为中奖而溢满幸福的笑脸，还有什么比这更暖呢？</p>

                      <p>中奖固然振奋人心，一张小小的彩票将会成为明星，为中奖者带来好运与财富，走上一段闪耀的旅程。但即使没有中奖，彩票的旅程也没有结束，每张彩票都会摇身一变，成为国家公益事业长河中的一滴水珠，为每一个需要帮助的人，送去温暖与关怀。以刮刮乐为例，一张面值2元的刮刮乐彩票，就会带来0.4元的公益金。2017年，即开型彩票总销售额逾126亿元，这就意味着，有超过25亿元的公益金在行“扶老”“助残”“救孤”“济困”之事。有了福彩公益金的支撑，各地方政府建设了大批重点社会福利事业项目，一座座阳光公寓、养老服务中心、社区居家养老服务站拔地而起，一场场慰问活动温暖着老年人的心，让老人老有所养，老有所安；各地福彩中心与残联等单位共同发起的“福彩有爱•光明行动” “爱心福彩助残创业”“福彩残疾人就业技能培训班”等多项活动，不仅解决了残疾人看病难的问题，更用福彩公益金帮助残疾人自主创业，用自己的双手来改变命运；而“福彩有爱•走进爱心庄园”等活动，给孤儿们带来了公益金的资助，也带来了阳光和欢笑，福彩公益金在这些福利院中化身为弱势儿童的守护天使，呵护着他们阳光健康地成长；公益金的“济困之手”，也让众多被疾病困扰的家庭看到了希望，为贫困乡村带来寒冬中的温暖，多少弱势人群因此走出了绝望与无助，因为他们知道，有福彩在他们背后默默支撑。随着彩票化身的公益金旅行到全国各地、村村寨寨，需要帮助的人们终于得以实现“老有所依、困有所济、孤幼得救、伤残得助”，这种广济天下的无疆大爱，难道不比现在流行的那种信马由缰的随意“佛系”更符合“佛性”吗？</p>

                      <p>兜兜转转，兔走乌飞，历经千年岁月，踏遍山川大河，我们虽然只是一张张平凡的小小彩票，但我们的旅程却绝不平凡，不只因这旅途精彩悠长，更因为我们的每一场旅行，都是充满暖意的佛系之旅，都是一场爱的奉献。</p>
