                       <p>黄树贤表示，自2016年2月国务院印发《关于加强农村留守儿童关爱保护工作的意见》以来，民政部会同有关部门，摸清了全国农村留守儿童底数，开发启用全国留守儿童信息系统，加强数据动态管理。民政部等8部委开展了“合力监护、相伴成长”关爱保护专项行动，帮助76万多名无人监护的农村留守儿童落实委托监护责任人，帮助18万名无户籍农村留守儿童登记户口，帮助1.6万名失辍学农村留守儿童返校复学，基本杜绝农村留守儿童无人监护现象。</p>

