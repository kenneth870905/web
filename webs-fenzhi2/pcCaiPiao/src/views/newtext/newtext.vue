                       <p>黄树贤说，社会组织是我国社会主义现代化建设的重要力量。党中央对社会组织管理的方针非常明确，就是加强党对社会组织工作的领导，坚持一手抓积极引导发展、一手抓严格依法管理，引导社会组织更好地服务国家、服务社会、服务群众。</p>

